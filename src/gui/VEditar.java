    private void BregistroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BregistroActionPerformed
        try {
            if(!this.jPasswordField1.getText().isEmpty() && !this.jPasswordField2.getText().isEmpty()){
                if(this.jPasswordField1.getText().equals(this.jPasswordField2.getText())){
                    if(!this.fa.getServer().Editar(this.id, this.jPasswordField2.getText())){
                        Merror.setText("Error al cambiar la clave");
                    }else{
                        this.dispose();
                    }
                }else{
                    Merror.setText("Contrase単as diferentes");
                    Merror.setVisible(true);
                }
            }else{
                Merror.setText("Completa todos los campos");
                Merror.setVisible(true);
            }
        } catch (RemoteException ex) {
            Logger.getLogger(VEditar.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_BregistroActionPerformed

